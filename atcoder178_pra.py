# C問題

n = int(input())

# n=1のとき、条件を満たすものは存在しない
if n==1:
    print(0)
# ベン図を使用した解法
# 全体 - (0無しの数列 + 9無しの数列 -　0,9両方無しの数列) = 0と9が共に存在する数列
else:
    print(((10**n)+(8**n)-(2*(9**n)))%((10**9)+7))
