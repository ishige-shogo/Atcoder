# B問題

# 座標(a, b)から座標(c, d)に向けてビリヤードの球を打つ
a, b, c, d = map(int, input().split())

# 相似な三角形の性質を利用。三角形のx軸上の部分に当たる辺の長さを計算
x = (b / (b + d)) * abs(c - a)

# a, cの大小によって射出する方向が異なるので条件分岐
if a < c:
    print(a + x)
else:
    print(a - x)

